import math

# Math Functions to perform Math Operations

p = 3.14156
q = 4
r = 5

# Round Function to the value mentioned

roundP = round(p, 2)
print(roundP)

# Power Function [x to the power y]

power = pow(4, 5)
print(power)

# Max Function

maximum = max(p, q, r)
print(maximum)

# Min Function

minimum = min(p, q, r)
print(minimum)

# Value of PI using Math Module

print(math.pi)

# Square Root of a number

squareRoot = math.sqrt(q)
print(squareRoot)

# Ceil Function: Rounds a floating point number upwards

roundUp = math.ceil(p)
print(roundUp)

# Floor Function: Rounds a floating point number downwards

roundDown = math.floor(p)
print(roundDown)